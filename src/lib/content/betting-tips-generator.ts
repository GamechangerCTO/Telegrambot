  /**
   * üéØ NEW: Get targeted match based on channel preferences (leagues/teams)
   */
  private async getTargetedMatchForChannel(request: BettingTipRequest) {
    try {
      const { createClient } = await import('@supabase/supabase-js');
      const supabase = createClient(
        process.env.NEXT_PUBLIC_SUPABASE_URL!,
        process.env.SUPABASE_SERVICE_ROLE_KEY!
      );

      console.log(`üîç Searching for matches based on channel preferences...`);

      // Build query based on channel preferences
      let query = supabase
        .from('daily_important_matches')
        .select('*')
        .eq('discovery_date', new Date().toISOString().split('T')[0])
        .order('importance_score', { ascending: false });

      // Filter by selected teams first (highest priority)
      if (request.selectedTeams?.length) {
        console.log(`üéØ Filtering by selected teams: ${request.selectedTeams.join(', ')}`);
        query = query.or(
          request.selectedTeams
            .map(team => `home_team.ilike.%${team}%,away_team.ilike.%${team}%`)
            .join(',')
        );
      }

      const { data: matches, error } = await query.limit(10);

      if (error) {
        console.error('‚ùå Error fetching targeted matches:', error);
        return null;
      }

      if (!matches || matches.length === 0) {
        console.log(`‚ö†Ô∏è No matches found for channel preferences`);
        return null;
      }

      // Additional filtering by leagues if specified
      let filteredMatches = matches;
      if (request.selectedLeagues?.length) {
        console.log(`üéØ Further filtering by selected leagues: ${request.selectedLeagues.length} leagues`);
        
        // Get league details to match by name
        const { data: leagues } = await supabase
          .from('leagues')
          .select('id, name, display_name')
          .in('id', request.selectedLeagues);

        if (leagues?.length) {
          const leagueNames = leagues.map(l => l.name?.toLowerCase() || l.display_name?.toLowerCase()).filter(Boolean);
          filteredMatches = matches.filter(match => 
            leagueNames.some(leagueName => 
              match.competition?.toLowerCase().includes(leagueName)
            )
          );
        }
      }

      if (filteredMatches.length === 0) {
        console.log(`‚ö†Ô∏è No matches found after league filtering`);
        return null;
      }

      // Return the highest importance score match that matches preferences
      const bestMatch = filteredMatches[0];
      console.log(`‚úÖ Found targeted match: ${bestMatch.home_team} vs ${bestMatch.away_team} (Score: ${bestMatch.importance_score})`);
      
      return bestMatch;

    } catch (error) {
      console.error('‚ùå Error getting targeted match for channel:', error);
      return null;
    }
  }

  /**
   * üìÖ Get today's most important match from database for betting
   */
  private async getDailyImportantMatch() {
    try {
      const { createClient } = await import('@supabase/supabase-js');
      const supabase = createClient(
        process.env.NEXT_PUBLIC_SUPABASE_URL!,
        process.env.SUPABASE_SERVICE_ROLE_KEY!
      );

      const { data: matches, error } = await supabase
        .from('daily_important_matches')
        .select('*')
        .eq('discovery_date', new Date().toISOString().split('T')[0])
        .gte('kickoff_time', new Date().toISOString()) // Only future matches
        .order('importance_score', { ascending: false })
        .order('kickoff_time', { ascending: true })
        .limit(1);

      if (error) {
        console.error('‚ùå Error fetching daily matches:', error);
        return null;
      }

      return matches && matches.length > 0 ? matches[0] : null;
      
    } catch (error) {
      console.error('‚ùå Error accessing daily matches database:', error);
      return null;
    }
  }

